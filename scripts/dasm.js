function toHex8(x) {
	if (x === undefined)
		throw ("NO X");
	var s = x.toString(16).toUpperCase();
	return "0".slice(s.length - 1) + s;
}

export function Dasm(data) {
	let op = 0;
	let n = 0;
	let nn = 0;
	let d = 0;
	let e = 0;
	let isFDorDD = false;
	if (data.length <= 2) {
		let reader = data[0];
		let pc = data[1];
		let opcodeb2;
		let op_displ = -1;
		let opcode = reader(pc++);
		if (opcode == 0xDD || opcode == 0xFD) {
			do {
				// DD* FD*
				opcodeb2 = reader(pc++);
				// DDDD, DDFD, FDDD, FDFD handle first byte as NOP
				if (opcodeb2 == 0xDD || opcodeb2 == 0xFD) {
					opcode = opcodeb2;
					continue;
				}
				opcode = (opcode << 8) | opcodeb2;
				// DDCB????, FDCB????
				if (opcode == 0xFDCB || opcode == 0xDDCB) {
					opcode = (opcode << 8) | reader(pc + 1);
				}
				// DD??, FD??
				else {
					// DD and FD falls back to regular instructions
					isFDorDD = true;
				}
				break;
			} while (opcode == 0xDD || opcode == 0xFD);
		}
		else if (opcode == 0xED || opcode == 0xCB) {
			opcode = (opcode << 8) | reader(pc++);
		}
		op = OpCodes.get(opcode);
		if (!op) {
			if (isFDorDD) {
				op = OpCodes.get(opcode & 0xFF);
				pc += 1;
			}
		}
		if (!op) {
			console.log("INVALID DASM OPCODE!");
			return;
		}
		n = -300;
		for (let chi in op[0]) {
			let chc = op[0][chi];
			switch (chc) {
				case 'd':
					d = reader(pc++);
					break;
				case 'e':
					e = reader(pc++);
					break;
				case 'n':
					if (n < -255) {
						n = reader(pc++);
					}
					else {
						nn = (reader(pc++) << 8) | n;
					}
					break;
			}
		}
	}
	else {
		op = OpCodes.get(data[1]);
		n = data[2];
		nn = data[3];
		e = data[4];
		d = data[5];
	}
	let rescode = op[0];
	let restxt = op[1];
	if (rescode.indexOf("d") != -1) {
		d = d & 0xFF;
		rescode = rescode.replace("d", toHex8(d));
		if (d & 0x80) d = -((~d + 1) & 0xFF);
		restxt = restxt.replace("d", d);
		if (d < 0) {
			restxt = restxt.replace("+", "");
		}
	}
	if (rescode.indexOf("e") != -1) {
		e = e & 0xFF;
		rescode = rescode.replace("e", toHex8(e));
		if (e & 0x80) e = -((~e + 1) & 0xFF);
		restxt = restxt.replace("e", e);
		if (e < 0) {
			restxt = restxt.replace("+", "");
		}
	}
	if (rescode.indexOf("n n") != -1) {
		rescode = rescode.replace("n", toHex8((nn & 0xFF) >> 0));
		rescode = rescode.replace("n", toHex8((nn & 0xFF00) >> 8));
		restxt = restxt.replace("n", toHex8((nn & 0xFF00) >> 8));
		restxt = restxt.replace("n", toHex8(nn & 0xFF));
	}
	if (rescode.indexOf("n") != -1) {
		rescode = rescode.replace("n", toHex8(n));
		restxt = restxt.replace("n", toHex8(n));
	}
	return [rescode + "          ".substr(0, 12 - rescode.length) + restxt, op[2]];
}

function codeToBytes(code) {
	if (code == 0) return [0];
	let res = [];
	while (code) {
		res.unshift(code & 0xff);
		code >>= 8;
	}
	return res;
}
export class Asm {
	constructor() {

		this.addr = 0;
		this.bytes = [];
		this.orgs = [];
		this.last = [];
		this.instr = {};
		// 	16632654: ["FDCB d 4E", "BIT 1,(IY+d)", 4],
		// matchers
		this.mOrg = /^\s*org\s+(?<addr>[a-fA-F0-9]{1,4})\s*$/;
		this.mInstr = /^\s*(?<instr>[a-zA-Z]+)\s*$/;

		// build a search tree from the opcodes
		OpCodes.forEach((v, k, _) => this.storeMnem(k, v));
	}
	storeMnem(code, mnem) {
		let iCode, iTxt, iBytes;
		[iCode, iTxt, iBytes] = mnem;
		let r;
		if (r = iTxt.match(this.mInstr)) {
			this.instr[r.groups.instr] = {
				bytes: codeToBytes(code),
				full: mnem,
			}
		}
		else {
			//throw ("unrecognised instr: " + iTxt);
			return;
		}
	}
	asm(txt) {
		this.last = [];
		let r;
		if (r = txt.match(this.mOrg)) {
			this.addr = parseInt(r.groups.addr, 16);
			this.orgs.push([this.addr, this.bytes.length]);
		}
		else if (r = txt.match(this.mInstr)) {
			let instr = this.instr[r.groups.instr];
			this.last = instr.bytes.slice();
		}
		else {
			return 'unkown instruction';
		}
		if (this.last.length) {
			this.bytes = this.bytes.concat(this.last);
		}
		return this.last.length;
	}
}
